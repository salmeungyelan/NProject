const VALIDATE = {
	email:
		/^(([^<>()[\].,;:\s@"]+(\.[^<>()[\].,;:\s@"]+)*)|(".+"))@(([^<>()[\].,;:\s@"]+\.)+[^<>()[\].,;:\s@"]{2,})$/i,
	nickname: /^(?=.*[a-z0-9가-힣])[a-z0-9가-힣]{2,16}$/,
	password: /^(?=.*[a-zA-Z])(?=.*[0-9])(?=.*[!@#$%^&*?_]).{8,16}$/,
	phone: /^01([0|1|6|7|8|9])-?([0-9]{3,4})-?([0-9]{4})$/,
	brn: /^[0-9]{3}-[0-9]{2}-[0-9]{5}$/,
};

export default VALIDATE;
